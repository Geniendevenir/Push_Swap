#include "libft.h"
#include <unistd.h>
#include <stdio.h>

int	ft_toupper(int ch);

int	ft_toupper(int ch)
{
	if (ch >= 97 && ch <= 122)
	{
		ch = ch - 32;
		return (ch);
	}
	return (ch);
}

int main()
{
	int		ch = 'q';

	printf("%c", ft_toupper(ch));
}